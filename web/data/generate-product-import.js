console.log(`VALUES`);

const productValues = products.map((p, i) => {
  const hsn = parseHsn(p.hsnCode);
  const sku = `DT-${p.productId}`;
  return `  ('${productUuid(i)}', '${esc(p.name)}', '${sku}', '${p.hsnCode}', ${p.salRate}, ${p.purRate}, ${p.closing}, 'pcs', true, ${p.barcode ? `'${p.barcode}'` : 'NULL'}, '${ENTITY_ID}', 'SINGLE', false, true, '${esc(p.cleanCategory)}', '${hsn.chapter}', '${hsn.heading}')`;
});
console.log(productValues.join(',\n'));
console.log(`ON CONFLICT (id) DO NOTHING;`);
console.log(``);

