// Generates 075_vendor_products.sql from Stock Register + category-map
// Usage: node data/generate-product-import.js > supabase/migrations/075_vendor_products.sql

const fs = require('fs');
const path = require('path');

// Load category map
const catMap = JSON.parse(fs.readFileSync(path.join(__dirname, 'category-map.json'), 'utf8'));
const catLookup = {};
catMap.forEach(e => { catLookup[e.raw] = e; });

// Load xlsx data (exported via npx xlsx-cli)
const xlsxPath = path.join(__dirname, 'stock-register.json');
const xlsxRaw = fs.readFileSync(xlsxPath, 'utf8');
const rows = JSON.parse(xlsxRaw.replace(/^Sheet\n/, ''));

// Entity ID for Dawai Tshongkhang
const ENTITY_ID = 'a0000000-0000-4000-8000-000000000004';

// Parse rows - skip header rows (first 2 entries)
const products = [];
for (let i = 2; i < rows.length; i++) {
  const r = rows[i];
  const name = r['      ']?.trim();
  const brand = r['__EMPTY_2']?.trim();
  const subGroup = r['__EMPTY_4']?.trim();
  const productId = r['__EMPTY_13'];
  const purRate = parseFloat(r['__EMPTY_18']) || 0;
  const salRate = parseFloat(r['__EMPTY_19']) || 0;
  const closing = parseFloat(r['__EMPTY_20']) || 0;
  const barcode = r['__EMPTY_25']?.toString().trim();

  if (!name || !subGroup) continue;

  const mapping = catLookup[subGroup];
  if (!mapping) {
    console.error(`WARNING: No mapping for sub-group "${subGroup}" (product: ${name})`);
    continue;
  }

  products.push({
    name,
    brand: brand || '',
    subGroup,
    cleanCategory: mapping.clean,
    hsnCode: mapping.hsn_code,
    productId: productId?.toString().padStart(4, '0'),
    purRate,
    salRate,
    closing: Math.round(closing),
    barcode: barcode && barcode !== 'undefined' ? barcode : '',
  });
}

// Generate deterministic UUIDs based on product index (valid v4)
// Use 'cc' prefix to avoid collision with demo data
function productUuid(idx) {
  const n = (idx + 1).toString().padStart(10, '0');
  return `cc${n.slice(0,6)}-${n.slice(6,10)}-4000-8000-cc${n.slice(0,10)}`;
}

// Escape SQL strings
function esc(s) {
  return s.replace(/'/g, "''");
}

// Build HSN chapter/heading/subheading from code
function parseHsn(code) {
  const parts = code.split('.');
  return {
    chapter: parts[0]?.substring(0, 2) || '',
    heading: parts[0]?.substring(0, 4) || '',
    subheading: parts[0] || '',
  };
}

